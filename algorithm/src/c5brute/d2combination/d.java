package c5brute.d2combination;

public class d {
    public static void main(String[] args) {
        int sum = 0;
        while (true) {
            for (int i = 0; i < 10; i++) {
                while (i != -1) {
                    break;
                }
                sum += i;
                if (i > 5) {
                    break;
                }
            }
            break;
        }
        System.out.println(sum);

    }
}
